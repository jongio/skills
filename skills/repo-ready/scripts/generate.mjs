/**
 * Fetch .gitignore content from the gitignore.io API.
 * Falls back to a bundled minimal template on failure.
 *
 * @param {string[]} templates - Template names (e.g., ['node', 'macos']).
 * @returns {Promise<string>}
 */
export async function fetchGitignore(templates) {
  const url = `https://www.toptal.com/developers/gitignore/api/${templates.join(',')}`;
  try {
    const res = await fetch(url, { signal: AbortSignal.timeout(10_000) });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    return await res.text();
  } catch {
    // Fallback: return a minimal .gitignore
    return [
      '# Generated by repo-ready (offline fallback)',
      'node_modules/',
      'dist/',
      'build/',
      '.env',
      '.env.local',
      '.DS_Store',
      'Thumbs.db',
      '*.log',
      '',
    ].join('\n');
  }
}

/**
 * Fetch a license body from the GitHub Licenses API.
 *
 * @param {string} spdxId - License SPDX identifier (e.g., 'mit').
 * @param {{ owner: string, year: string|number }} vars - Template variables.
 * @returns {Promise<string>}
 */
export async function fetchLicense(spdxId, vars = {}) {
  const url = `https://api.github.com/licenses/${spdxId}`;
  try {
    const res = await fetch(url, {
      headers: { Accept: 'application/vnd.github+json' },
      signal: AbortSignal.timeout(10_000),
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    let body = data.body || '';
    body = body.replace(/\[year\]/g, String(vars.year || new Date().getFullYear()));
    body = body.replace(/\[fullname\]/g, vars.owner || 'OWNER');
    return body;
  } catch {
    return `License: ${spdxId.toUpperCase()}\nCopyright (c) ${vars.year || new Date().getFullYear()} ${vars.owner || 'OWNER'}\n\nSee https://choosealicense.com/licenses/${spdxId}/ for full text.\n`;
  }
}

