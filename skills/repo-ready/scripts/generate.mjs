/**
 * Write a file, creating parent directories as needed.
 * Returns true if the file was created, false if it already existed.
 *
 * @param {string} filePath - Absolute path.
 * @param {string} content - File content.
 * @param {{ force?: boolean }} opts
 * @returns {boolean}
 */
export function safeWrite(filePath, content, opts = {}) {
  if (existsSync(filePath) && !opts.force) {
    return false;
  }
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, content, 'utf-8');
  return true;
}

/**
 * Generate all files based on configuration.
 *
 * @param {string} dir - Target directory.
 * @param {object} config - Generation configuration.
 * @returns {Promise<Array<{ path: string, created: boolean, skipped: string|null }>>}
 */
export async function generateAll(dir, config) {
  const results = [];
  const write = (relPath, content) => {
    const abs = join(dir, relPath);
    const created = safeWrite(abs, content, { force: config.force });
    results.push({
      path: relPath,
      created,
      skipped: created ? null : 'already exists',
    });
  };

  const skip = config.skip || new Set();
  const stacks = config.stacks || [];

  // .gitignore
  if (!skip.has('gitignore')) {
    const templates = gitignoreTemplates(stacks);
    const content = await fetchGitignore(templates);
    write('.gitignore', content);
  }

  // .gitattributes
  if (!skip.has('gitattributes')) {
    write('.gitattributes', generateGitattributes(stacks));
  }

  // LICENSE
  if (!skip.has('license') && config.license) {
    const content = await fetchLicense(config.license, {
      owner: config.owner,
      year: config.year,
    });
    write('LICENSE', content);
  }

  // README.md
  if (!skip.has('readme')) {
    write('README.md', generateReadme({
      name: config.name,
      description: config.description,
      stacks,
    }));
  }

  // CONTRIBUTING.md
  if (!skip.has('contributing')) {
    write('CONTRIBUTING.md', generateContributing({
      name: config.name,
      stacks,
      pkgManager: config.pkgManager,
    }));
  }

  // CODE_OF_CONDUCT.md
  if (!skip.has('code-of-conduct')) {
    write('CODE_OF_CONDUCT.md', generateCodeOfConduct({
      contactEmail: config.cocContact,
    }));
  }

  // SECURITY.md
  if (!skip.has('security')) {
    write('SECURITY.md', generateSecurity({
      contactEmail: config.securityContact,
      name: config.name,
    }));
  }

  // Issue templates
  if (!skip.has('bug-report-template')) {
    write('.github/ISSUE_TEMPLATE/bug_report.yml', generateBugReportTemplate());
  }
  if (!skip.has('feature-request-template')) {
    write('.github/ISSUE_TEMPLATE/feature_request.yml', generateFeatureRequestTemplate());
  }
  if (!skip.has('issue-template-config')) {
    write('.github/ISSUE_TEMPLATE/config.yml', generateIssueTemplateConfig({
      blankIssues: config.blankIssues,
      discussionsUrl: config.discussionsUrl,
    }));
  }

  // PR template
  if (!skip.has('pr-template')) {
    write('.github/PULL_REQUEST_TEMPLATE.md', generatePRTemplate());
  }

  // dependabot.yml
  if (!skip.has('dependabot')) {
    const ecosystems = dependabotEcosystems(stacks);
    write('.github/dependabot.yml', generateDependabot(ecosystems, {
      schedule: config.dependabotSchedule,
    }));
  }

  // CI workflow
  if (!skip.has('ci-workflow')) {
    const workflow = generateCIWorkflow(stacks, {
      pkgManager: config.pkgManager,
    });
    if (workflow) {
      write('.github/workflows/ci.yml', workflow);
    }
  }

  // CODEOWNERS
  if (!skip.has('codeowners')) {
    write('.github/CODEOWNERS', generateCodeowners(config.codeowners));
  }

  // FUNDING.yml
  if (!skip.has('funding') && config.funding) {
    write('.github/FUNDING.yml', generateFunding(config.funding));
  }

  // CHANGELOG.md
  if (!skip.has('changelog')) {
    write('CHANGELOG.md', generateChangelog({ name: config.name }));
  }

  // .editorconfig
  if (!skip.has('editorconfig')) {
    write('.editorconfig', generateEditorconfig(stacks));
  }

  return results;
}

