/**
 * Generate a basic CI workflow based on the primary detected stack.
 *
 * @param {string[]} stacks - Detected stacks.
 * @param {{ pkgManager?: string }} opts
 * @returns {string}
 */
export function generateCIWorkflow(stacks, opts = {}) {
  const primary = stacks[0];
  if (!primary) return null;

  const header = `name: CI

on:
  push:
    branches: [main]
  pull_request:
    branches: [main]

`;

  const templates = {
    node: () => {
      const pm = opts.pkgManager || 'npm';
      const install = pm === 'pnpm'
        ? `      - uses: pnpm/action-setup@v4\n      - run: pnpm install --frozen-lockfile`
        : pm === 'yarn'
          ? '      - run: yarn install --frozen-lockfile'
          : '      - run: npm ci';
      const test = pm === 'pnpm' ? 'pnpm test' : pm === 'yarn' ? 'yarn test' : 'npm test';
      return `${header}jobs:
  build:
    runs-on: ubuntu-latest
    strategy:
      matrix:
        node-version: [20.x, 22.x]
    steps:
      - uses: actions/checkout@v4
      - name: Use Node.js \${{ matrix.node-version }}
        uses: actions/setup-node@v4
        with:
          node-version: \${{ matrix.node-version }}
${install}
      - run: ${pm} run build --if-present
      - run: ${test}
`;
    },

    python: () => `${header}jobs:
  test:
    runs-on: ubuntu-latest
    strategy:
      matrix:
        python-version: ["3.11", "3.12", "3.13"]
    steps:
      - uses: actions/checkout@v4
      - name: Set up Python \${{ matrix.python-version }}
        uses: actions/setup-python@v5
        with:
          python-version: \${{ matrix.python-version }}
      - run: pip install -e ".[dev]"
      - run: pytest
`,

    go: () => `${header}jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-go@v5
        with:
          go-version-file: go.mod
      - run: go build -v ./...
      - run: go test -v ./...
`,

    rust: () => `${header}jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - name: Build
        run: cargo build --verbose
      - name: Run tests
        run: cargo test --verbose
      - name: Check formatting
        run: cargo fmt -- --check
      - name: Clippy
        run: cargo clippy -- -D warnings
`,

    dotnet: () => `${header}jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-dotnet@v4
        with:
          dotnet-version: 9.0.x
      - run: dotnet restore
      - run: dotnet build --no-restore
      - run: dotnet test --no-build --verbosity normal
`,

    ruby: () => `${header}jobs:
  test:
    runs-on: ubuntu-latest
    strategy:
      matrix:
        ruby-version: ["3.2", "3.3"]
    steps:
      - uses: actions/checkout@v4
      - uses: ruby/setup-ruby@v1
        with:
          ruby-version: \${{ matrix.ruby-version }}
          bundler-cache: true
      - run: bundle exec rake test
`,

    java: () => `${header}jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4
      - uses: actions/setup-java@v4
        with:
          distribution: temurin
          java-version: 21
      - name: Build with Gradle
        run: ./gradlew build
`,
  };

  const gen = templates[primary];
  return gen ? gen() : null;
}

/**
 * Generate .github/CODEOWNERS.
 *
 * @param {Array<{ pattern: string, owners: string[] }>} entries
 * @returns {string}
 */
export function generateCodeowners(entries) {
  if (!entries || entries.length === 0) {
    return '# CODEOWNERS\n# Add path patterns and owner GitHub usernames/teams.\n# See: https://docs.github.com/en/repositories/managing-your-repositorys-settings-and-features/customizing-your-repository/about-code-owners\n\n* @OWNER\n';
  }

  const lines = [
    '# CODEOWNERS',
    '# Automatically request reviews from these owners when files change.',
    '# See: https://docs.github.com/en/repositories/managing-your-repositorys-settings-and-features/customizing-your-repository/about-code-owners',
    '',
  ];
  for (const e of entries) {
    lines.push(`${e.pattern}  ${e.owners.join(' ')}`);
  }
  lines.push('');
  return lines.join('\n');
}

/**
 * Generate .github/FUNDING.yml.
 *
 * @param {object} platforms - Platform-to-username mapping.
 * @returns {string}
 */
export function generateFunding(platforms) {
  const lines = ['# Funding platforms'];
  for (const [platform, value] of Object.entries(platforms)) {
    if (Array.isArray(value)) {
      lines.push(`${platform}: [${value.map(v => `"${v}"`).join(', ')}]`);
    } else {
      lines.push(`${platform}: ${value}`);
    }
  }
  lines.push('');
  return lines.join('\n');
}

