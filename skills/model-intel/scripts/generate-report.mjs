#!/usr/bin/env node
/**
 * generate-report.mjs
 *
 * Produces the full Model Intel report as a Markdown file.
 * Usage: node scripts/generate-report.mjs [--output path] [--quick]
 *
 * --quick: Skip web research, use built-in registry only.
 * --output: Custom output path (default: ./model-intel-report.md)
 */

import { MODEL_REGISTRY, PROVIDERS, groupByProvider, rankBy, recommendFor, TASK_PROFILES } from './registry.mjs';
import { barChart, table, radarChart, scatterPlot, timeline, headToHead, decisionTree } from './charts.mjs';

const args = process.argv.slice(2);
const quick = args.includes('--quick');
const outputIdx = args.indexOf('--output');
const outputPath = outputIdx >= 0 && args[outputIdx + 1] ? args[outputIdx + 1] : null;

/**
 * Generate the complete report content.
 */
function generateReport() {
  const now = new Date().toISOString().slice(0, 19).replace('T', ' ');
  const sections = [];

  // ─── Header ─────────────────────────────────────────────────────────────────
  sections.push(`# 🧠 Model Intelligence Report

> Generated: ${now}
> Models discovered: ${MODEL_REGISTRY.length}
> Providers: ${Object.keys(PROVIDERS).map(k => PROVIDERS[k].emoji + ' ' + PROVIDERS[k].name).join(', ')}
> Mode: ${quick ? 'Quick (registry only)' : 'Full (with research)'}

---
`);

  // ─── Section 1: Model Census ────────────────────────────────────────────────
  sections.push(generateCensus());

  // ─── Section 2: Architecture Cards ──────────────────────────────────────────
  sections.push(generateArchitectureCards());

  // ─── Section 3: Benchmark Dashboard ─────────────────────────────────────────
  sections.push(generateBenchmarkDashboard());

  // ─── Section 4: Capability Radar ────────────────────────────────────────────
  sections.push(generateCapabilityRadar());

  // ─── Section 5: Strengths & Weaknesses ──────────────────────────────────────
  sections.push(generateStrengthsWeaknesses());

  // ─── Section 6: Decision Tree ───────────────────────────────────────────────
  sections.push(generateDecisionTree());

  // ─── Section 7: Head-to-Head ────────────────────────────────────────────────
  sections.push(generateHeadToHead());

  // ─── Section 8: Cost/Performance Frontier ───────────────────────────────────
  sections.push(generateCostPerformance());

  // ─── Section 9: Context Window Guide ────────────────────────────────────────
  sections.push(generateContextGuide());

  // ─── Section 10: Release Timeline ───────────────────────────────────────────
  sections.push(generateTimeline());

  // ─── Section 11: Model Pairing Strategies ───────────────────────────────────
  sections.push(generatePairingStrategies());

  // ─── Section 12: Emerging Patterns ──────────────────────────────────────────
  sections.push(generateEmergingPatterns());

  // ─── Footer ─────────────────────────────────────────────────────────────────
  sections.push(`---

## Methodology

Scores are normalized 0-100 composites derived from:
- Official provider benchmarks (SWE-bench, HumanEval, MATH, MMLU)
- Independent evaluations (Aider polyglot, LiveCodeBench, LMSYS Arena)
- Community consensus from practical usage reports
- Copilot-specific performance observations (tool use, multi-file edits)

**Disclaimer**: Model capabilities evolve rapidly. This report reflects data available
at generation time. Re-run \`model-intel\` for an updated assessment.

---
*Generated by model-intel skill v1.0.0*
`);

  return sections.join('\n');
}

// ─── Section Generators ───────────────────────────────────────────────────────

function generateCensus() {
  const groups = groupByProvider();
  const rows = [];

  for (const [provKey, models] of groups) {
    for (const m of models) {
      rows.push([
        PROVIDERS[provKey].emoji,
        m.name,
        m.id,
        m.contextTiers.join(', '),
        m.effortLevels.join(', ') || 'n/a',
        `${m.contextWindow}K`,
        m.tier,
        m.releaseDate,
      ]);
    }
  }

  const t = table({
    title: '1. Model Census',
    headers: ['', 'Model', 'ID', 'Context Tiers', 'Effort Levels', 'Window', 'Tier', 'Released'],
    rows,
  });

  const summary = `
**Summary by provider:**
${[...groups.entries()].map(([k, v]) => `- ${PROVIDERS[k].emoji} ${PROVIDERS[k].name}: ${v.length} models`).join('\n')}

**By tier:**
- Standard (included): ${MODEL_REGISTRY.filter(m => m.tier === 'standard').length} models
- Premium (costs extra): ${MODEL_REGISTRY.filter(m => m.tier === 'premium').length} models
`;

  return `## 📋 Model Census\n${t}\n${summary}`;
}

function generateArchitectureCards() {
  const families = new Map();
  for (const m of MODEL_REGISTRY) {
    const key = `${m.provider}/${m.family}`;
    if (!families.has(key)) families.set(key, []);
    families.get(key).push(m);
  }

  const cards = [];
  for (const [key, models] of families) {
    const rep = models[0];
    const versions = models.map(m => m.name).join(', ');
    cards.push(`
### ${PROVIDERS[rep.provider].emoji} ${key.split('/')[1].toUpperCase()} Family

| Property | Value |
|----------|-------|
| Provider | ${PROVIDERS[rep.provider].name} |
| Models | ${versions} |
| Architecture | ${rep.architecture} |
| Context Window | ${rep.contextWindow}K tokens |
| Tier | ${rep.tier} |
`);
  }

  return `## 🏗️ Architecture Cards\n${cards.join('\n')}`;
}

function generateBenchmarkDashboard() {
  const dimensions = [
    { key: 'codeGeneration', label: 'Code Generation (SWE-bench, HumanEval proxy)' },
    { key: 'reasoning', label: 'Reasoning (MATH, ARC, BBH proxy)' },
    { key: 'codeReview', label: 'Code Review (precision/recall proxy)' },
    { key: 'multiFile', label: 'Multi-file Understanding' },
    { key: 'toolUse', label: 'Tool Use / Agentic Performance' },
  ];

  const charts = [];
  for (const dim of dimensions) {
    const ranked = rankBy(dim.key).slice(0, 10);
    const data = ranked.map(m => ({ name: m.name, value: m.scores[dim.key] }));
    charts.push(`\n#### ${dim.label}\n\n\`\`\`\n${barChart(data)}\n\`\`\``);
  }

  return `## 📊 Benchmark Dashboard\n\nNormalized scores (0-100) across key capability areas:\n${charts.join('\n')}`;
}

function generateCapabilityRadar() {
  // Show radar for top models from each provider
  const highlights = [
    MODEL_REGISTRY.find(m => m.id === 'claude-sonnet-5'),
    MODEL_REGISTRY.find(m => m.id === 'claude-opus-4.8'),
    MODEL_REGISTRY.find(m => m.id === 'gpt-5.6-sol'),
    MODEL_REGISTRY.find(m => m.id === 'gpt-5.5'),
    MODEL_REGISTRY.find(m => m.id === 'gemini-3.1-pro-preview'),
    MODEL_REGISTRY.find(m => m.id === 'gemini-3.5-flash'),
  ].filter(Boolean);

  const radars = highlights.map(m => `\n\`\`\`\n${radarChart({ modelName: m.name, scores: m.scores })}\n\`\`\``);

  return `## 🎯 Capability Radar\n\nMulti-dimensional profiles for flagship models:\n${radars.join('\n')}`;
}

function generateStrengthsWeaknesses() {
  const entries = MODEL_REGISTRY.map(m => {
    const scores = Object.entries(m.scores);
    const sorted = scores.sort((a, b) => b[1] - a[1]);
    const strengths = sorted.slice(0, 3).map(([k, v]) => `${formatDimLabel(k)} (${v})`);
    const weaknesses = sorted.slice(-3).reverse().map(([k, v]) => `${formatDimLabel(k)} (${v})`);

    return `
### ${PROVIDERS[m.provider].emoji} ${m.name}

| Strengths 💪 | Weaknesses 📉 |
|-------------|--------------|
| ${strengths[0]} | ${weaknesses[0]} |
| ${strengths[1]} | ${weaknesses[1]} |
| ${strengths[2]} | ${weaknesses[2]} |

> *${m.architecture}*
`;
  });

  return `## 💪📉 Strengths & Weaknesses Matrix\n${entries.join('')}`;
}

function generateDecisionTree() {
  const decisions = [];

  for (const [, profile] of Object.entries(TASK_PROFILES)) {
    const ranked = recommendFor(profile.weights);
    const best = ranked[0];
    const alt = ranked[1];
    decisions.push({
      task: profile.label,
      recommended: `${best.name} (${best.tier})`,
      fallback: `${alt.name} (${alt.tier})`,
      reason: explainChoice(best, profile.weights),
    });
  }

  const tree = decisionTree(decisions);

  return `## 🌳 Decision Tree: Which Model for What?\n\n\`\`\`\n${tree}\n\`\`\``;
}

function generateHeadToHead() {
  const matchups = [
    ['claude-sonnet-4.6', 'claude-sonnet-5'],
    ['gpt-5.5', 'claude-opus-4.6'],
    ['gemini-3.5-flash', 'claude-haiku-4.5'],
    ['gpt-5.6-sol', 'claude-opus-4.8'],
    ['gpt-5.4-mini', 'gpt-5-mini'],
    ['claude-sonnet-5', 'gpt-5.6-terra'],
  ];

  const comparisons = matchups.map(([idA, idB]) => {
    const a = MODEL_REGISTRY.find(m => m.id === idA);
    const b = MODEL_REGISTRY.find(m => m.id === idB);
    if (!a || !b) return '';
    return `\n\`\`\`\n${headToHead(a, b)}\n\`\`\``;
  }).filter(Boolean);

  return `## ⚔️ Head-to-Head Comparisons\n\nCommon model matchups for frequently asked "which one?" questions:\n${comparisons.join('\n')}`;
}

function generateCostPerformance() {
  const points = MODEL_REGISTRY.map(m => {
    const avgQuality = Object.values(m.scores).reduce((s, v) => s + v, 0) / Object.values(m.scores).length;
    return { name: m.name, x: m.scores.costEfficiency, y: Math.round(avgQuality) };
  });

  const chart = scatterPlot(points, {
    xLabel: 'Cost Efficiency →',
    yLabel: '↑ Average Quality',
  });

  // Frontier analysis
  const frontier = [...points].sort((a, b) => {
    const scoreA = a.x * 0.4 + a.y * 0.6;
    const scoreB = b.x * 0.4 + b.y * 0.6;
    return scoreB - scoreA;
  });

  const analysis = `
**Pareto frontier (best quality per cost):**
${frontier.slice(0, 5).map((p, i) => `${i + 1}. ${p.name} (quality: ${p.y}, efficiency: ${p.x})`).join('\n')}

**Best value picks:**
- 🏆 Best overall value: ${frontier[0].name}
- ⚡ Best for budget-conscious: ${points.sort((a, b) => b.x - a.x)[0].name}
- 🎯 Best quality regardless of cost: ${points.sort((a, b) => b.y - a.y)[0].name}
`;

  return `## 💰 Cost/Performance Frontier\n\n\`\`\`\n${chart}\n\`\`\`\n${analysis}`;
}

function generateContextGuide() {
  const rows = MODEL_REGISTRY
    .sort((a, b) => b.contextWindow - a.contextWindow)
    .map(m => [
      m.name,
      `${m.contextWindow}K`,
      m.contextTiers.includes('long_context') ? '✅' : '❌',
      contextGuidance(m),
    ]);

  const t = table({
    headers: ['Model', 'Window', 'Long Context', 'Practical Guidance'],
    rows,
  });

  return `## 📏 Context Window Guide

${t}

### Effective context utilization tips:

- **1M token models** (Gemini): Excellent for whole-repo analysis, but quality
  can degrade in the "lost middle" zone (tokens 200K-800K). Front-load important
  context.
- **256K models** (GPT-5.4/5.5/5.6): Sweet spot for large PRs and multi-file
  refactors. Needle-in-haystack performance is strong throughout.
- **200K models** (Claude family): Consistent quality across the full window.
  Best attention distribution of any provider at their context size.
- **128K models** (GPT-5 mini, MAI-Code): Sufficient for most single-file and
  small multi-file tasks. Context quality is high but capacity limits
  whole-repo analysis.

### When to use long_context tier:

Use \`context_tier: "long_context"\` when:
- Analyzing more than 50 files simultaneously
- PR diffs exceed 5000 lines
- The task requires understanding relationships across distant modules
- You're doing whole-codebase searches or architectural analysis
`;
}

