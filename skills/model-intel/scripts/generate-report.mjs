// ─── Helpers ──────────────────────────────────────────────────────────────────

function formatDimLabel(dim) {
  return dim.replace(/([A-Z])/g, ' $1').replace(/^./, s => s.toUpperCase()).trim();
}

function explainChoice(model, weights) {
  const topWeight = Object.entries(weights).sort((a, b) => b[1] - a[1])[0];
  return `Tops in ${formatDimLabel(topWeight[0]).toLowerCase()} (score: ${model.scores[topWeight[0]]})`;
}

function contextGuidance(model) {
  if (model.contextWindow >= 1000) return 'Whole-repo analysis, massive PRs';
  if (model.contextWindow >= 256) return 'Large multi-file tasks, big PRs';
  if (model.contextWindow >= 200) return 'Standard multi-file, most tasks';
  return 'Single-file focus, small tasks';
}

// ─── Main ─────────────────────────────────────────────────────────────────────

const report = generateReport();

if (outputPath) {
  const { writeFileSync } = await import('node:fs');
  writeFileSync(outputPath, report, 'utf8');
  console.log(`Report written to: ${outputPath}`);
} else {
  // Print to stdout for piping or direct consumption
  console.log(report);
}
